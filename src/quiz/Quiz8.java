package quiz;

public class Quiz8 {

	public static void main(String[] args) {
		int a = 10;
		double b = 2.0;
		
		int p = (int) (a + b);
		int m = a - (int) b;
		int x = a * (int) b;
		int c = a / (int) b;
		// 양쪽의 데이터형을 맞춰줘야함!!
		System.out.println(p);
		System.out.println(m);
		System.out.println(x);
		System.out.println(c);

	}

}

